-- gitsigns keymaps
local function map(mode, l, r, opts)
  opts = opts or {}
  opts.noremap = true
  opts.silent = true
  vim.api.nvim_set_keymap(mode, l, r, opts)
end

map('n', ']c', ':lua require("gitsigns").next_hunk()<CR>', {})
map('n', '[c', ':lua require("gitsigns").prev_hunk()<CR>', {})

